  CREATE OR REPLACE FORCE EDITIONABLE JSON RELATIONAL DUALITY VIEW "APP_USER"."POTVRDA_O_PRIJAVI_JSON"  AS 
  SELECT JSON
{
  '_id'          : p.broj_prijave,
  'datumPrijave' : p.datum_prijave WITH UPDATE,

  'kandidat' :
  (
    SELECT JSON
    {
      'jmbg'         : k.jmbg,
      'ime'          : k.ime,
      'prezime'      : k.prezime,
      'imeRoditelja' : k.ime_roditelja,

      'pol'     : k.pol WITH UPDATE,
      'adresa'  : k.adresa WITH UPDATE,
      'mobilni' : k.mobilni_telefon WITH UPDATE,
      'fiksni'  : k.fiksni_telefon WITH UPDATE,

      'diplomiranja' :
      [
        SELECT JSON
        {
          'brojIndeksa'       : d.broj_indeksa,
          'godinaUpisa'       : d.godina_upisa,
          'prosek'            : d.prosek,
          'datumDiplomiranja' : d.datum_diplomiranja,

          'studijskiProgram' :
          (
            SELECT JSON
            {
              'sifraSP' : sp.sifra_sp,
              'nazivSP' : sp.naziv_sp,
              'zvanje'  : sp.zvanje,

              'fakultet' :
              (
                SELECT JSON
                {
                  'sifraFakulteta' : f.sifra_fakulteta,
                  'nazivFakulteta' : f.naziv_fakulteta,

                  'univerzitet' :
                  (
                    SELECT JSON
                    {
                      'sifraUniverziteta' : u.sifra_univerziteta,
                      'nazivUniverziteta' : u.naziv_univerziteta
                    }
                    FROM univerzitet u WITH NOINSERT NOUPDATE NODELETE
                    WHERE u.sifra_univerziteta = f.sifra_univerziteta
                    WITH CHECK OPTION
                  )
                }
                FROM fakultet f WITH NOINSERT NOUPDATE NODELETE
                WHERE f.sifra_fakulteta = d.sifra_fakulteta
                WITH CHECK OPTION
              )
            }
            FROM studijski_program sp WITH NOINSERT NOUPDATE NODELETE
            WHERE sp.sifra_sp = d.sifra_sp
            WITH CHECK OPTION
          )
        }
        FROM diplomiranje d WITH NOINSERT NOUPDATE NODELETE
        WHERE d.jmbg = k.jmbg
        WITH CHECK OPTION
      ]
    }
    FROM kandidat_prijava k WITH INSERT UPDATE
    WHERE k.broj_prijave = p.broj_prijave
    WITH CHECK OPTION
  )
}
FROM potvrda_o_prijavi p WITH INSERT UPDATE DELETE
;
